package com.myapp.ecommerce.entity;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;

import java.util.Map;
import java.util.Objects;

@DynamoDBTable(tableName = "order")
public class Order {

    public Order() {
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public boolean isFinished() {
        return isFinished;
    }

    public void setFinished(boolean finished) {
        isFinished = finished;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public Map<String, Integer> getItems() {
        return items;
    }

    public void setItems(Map<String, Integer> items) {
        this.items = items;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }


    @DynamoDBHashKey
    @DynamoDBAutoGeneratedKey
    private String orderId;

    public Order(String orderId, String userId, boolean isFinished, String status, Map<String, Integer> items, double price) {
        this.orderId = orderId;
        this.userId = userId;
        this.isFinished = isFinished;
        this.status = status;
        this.items = items;
        this.price = price;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Order order = (Order) o;
        return isFinished == order.isFinished && Double.compare(price, order.price) == 0 && Objects.equals(orderId, order.orderId) && Objects.equals(userId, order.userId) && Objects.equals(status, order.status) && Objects.equals(items, order.items);
    }

    public String getOrderId() {
        return orderId;
    }

    public void setOrderId(String orderId) {
        this.orderId = orderId;
    }

    @Override
    public int hashCode() {
        return Objects.hash(orderId, userId, isFinished, status, items, price);
    }

    @Override
    public String toString() {
        return "Order{" +
                "orderId='" + orderId + '\'' +
                ", userId='" + userId + '\'' +
                ", isFinished=" + isFinished +
                ", status='" + status + '\'' +
                ", items=" + items +
                ", price=" + price +
                '}';
    }

    @DynamoDBAttribute
    private String userId;
    @DynamoDBAttribute
    private boolean isFinished;
    @DynamoDBAttribute
    private String status;
    @DynamoDBAttribute
    private Map<String, Integer> items;
    @DynamoDBAttribute
    private double price;

}
