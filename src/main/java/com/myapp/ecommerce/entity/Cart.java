package com.myapp.ecommerce.entity;

import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAttribute;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBAutoGeneratedKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBHashKey;
import com.amazonaws.services.dynamodbv2.datamodeling.DynamoDBTable;
import java.util.Map;
import java.util.Objects;

@DynamoDBTable(tableName = "cart")
public class Cart {
    @DynamoDBHashKey
    @DynamoDBAutoGeneratedKey
    private String cartId;


    public String getCartId() {
        return cartId;
    }

    public void setCartId(String cartId) {
        this.cartId = cartId;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public Map<String, Integer> getItems() {
        return items;
    }

    public void setItems(Map<String, Integer> items) {
        this.items = items;
    }

    public Cart() {
    }


    @DynamoDBAttribute
    private String userId;

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Cart cart = (Cart) o;
        return Double.compare(price, cart.price) == 0 && Objects.equals(cartId, cart.cartId) && Objects.equals(userId, cart.userId) && Objects.equals(items, cart.items);
    }

    @Override
    public String toString() {
        return "Cart{" +
                "cartId='" + cartId + '\'' +
                ", userId='" + userId + '\'' +
                ", price=" + price +
                ", items=" + items +
                '}';
    }

    @Override
    public int hashCode() {
        return Objects.hash(cartId, userId, price, items);
    }

    public Cart(String cartId, String userId, double price, Map<String, Integer> items) {
        this.cartId = cartId;
        this.userId = userId;
        this.price = price;
        this.items = items;
    }

    @DynamoDBAttribute
    private double price;
    @DynamoDBAttribute
    private Map<String, Integer> items;
}
